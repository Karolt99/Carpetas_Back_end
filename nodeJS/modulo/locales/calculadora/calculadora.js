const sumar = function (numUno, NumDos){
    return numUno+NumDos
}

const restar = function (numUno, NumDos){
    return numUno-NumDos
}

const multiplicar = function (numUno, NumDos){
    return numUno*NumDos
}

const dividir = function (numUno, NumDos){
    return numUno/NumDos
}

module.exports={
    sumar,
    restar,
    multiplicar,
    dividir
}